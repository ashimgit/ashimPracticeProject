package sale.pojo;

public class SaleItemDetail {
	
	private String tagId;
	private String fruitId;
	private String quantity;
	private String saleUnit;
	private Double saleItemRate;
	private Double saleItemDiscount;
	private Double saleIteamTotal;
	
	public String getTagId() {
		return tagId;
	}
	public void setTagId(String tagId) {
		this.tagId = tagId;
	}
	public String getFruitId() {
		return fruitId;
	}
	public void setFruitId(String fruitId) {
		this.fruitId = fruitId;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getSaleUnit() {
		return saleUnit;
	}
	public void setSaleUnit(String saleUnit) {
		this.saleUnit = saleUnit;
	}
	public Double getSaleItemRate() {
		return saleItemRate;
	}
	public void setSaleItemRate(Double saleItemRate) {
		this.saleItemRate = saleItemRate;
	}
	public Double getSaleItemDiscount() {
		return saleItemDiscount;
	}
	public void setSaleItemDiscount(Double saleItemDiscount) {
		this.saleItemDiscount = saleItemDiscount;
	}
	public Double getSaleIteamTotal() {
		return saleIteamTotal;
	}
	public void setSaleIteamTotal(Double saleIteamTotal) {
		this.saleIteamTotal = saleIteamTotal;
	}
	
	
}
